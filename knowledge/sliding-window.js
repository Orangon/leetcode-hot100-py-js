// JavaScript：无重复字符的最长子串（LeetCode 3）
function lengthOfLongestSubstring(s) {
  const win = new Set();            // 窗口集合：记着窗口里现在有哪些字符
  let left = 0;                     // 窗口左边界（下标）
  let maxLen = 0;                   // 历史最长窗口的长度
  for (let right = 0; right < s.length; right++) {  // 右边界一格一格右移
    while (win.has(s[right])) {     // 新字符已在窗口里 → 必须收缩
      win.delete(s[left]);          //   把最左边的字符踢出集合
      left++;                       //   左边界右移一格
    }                               // 循环结束时，窗口里一定没有 s[right] 了
    win.add(s[right]);              // 放心把新字符吞进窗口
    maxLen = Math.max(maxLen, right - left + 1);  // 窗口长度 = 右 - 左 + 1，刷新纪录
  }
  return maxLen;                    // 走完全程，纪录就是答案
}
