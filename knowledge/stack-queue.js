// JavaScript：每日温度（LeetCode 739）
function dailyTemperatures(t) {
  const ans = new Array(t.length).fill(0);   // 答案数组，默认 0（等不到更热的天）
  const stack = [];                          // 单调栈：存还在等答案的下标，温度从底到顶递减
  for (let i = 0; i < t.length; i++) {       // 一天一天往后看
    while (stack.length > 0 && t[i] > t[stack[stack.length - 1]]) {
      // 今天比栈顶那天热 → 栈顶那天终于等到答案了
      const j = stack.pop();                 //   把那天从栈里请出去
      ans[j] = i - j;                        //   等的天数 = 今天下标 - 那天下标
    }
    stack.push(i);                           // 今天还没等到答案，进栈等
  }
  return ans;                                // 留在栈里的下标，答案保持 0
}
